    private void jButton27ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton27ActionPerformed
        // TODO add your handling code here:
        String firstName = jTextField21.getText();
        String lastName = jTextField22.getText();
        String age = jTextField24.getText();
        String contactnumber = jTextField23.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        ButtonModel genderSelection = buttonGroup1.getSelection();

        if (firstName.isEmpty()){
            JOptionPane.showMessageDialog(this, "Invalid First Name","Warning",JOptionPane.WARNING_MESSAGE);
        }else if(lastName.isEmpty()){
            JOptionPane.showMessageDialog(this, "Invalid Last Name","Warning",JOptionPane.WARNING_MESSAGE);
        }else if(password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Invalid Password","Warning",JOptionPane.WARNING_MESSAGE);
        }else if(age.isEmpty()){
            JOptionPane.showMessageDialog(this, "Invalid age","Warning",JOptionPane.WARNING_MESSAGE);
        }else if(genderSelection==null){
            JOptionPane.showMessageDialog(this, "Invalid Gender","Warning",JOptionPane.WARNING_MESSAGE);
        }else if(contactnumber.isEmpty()){
            JOptionPane.showMessageDialog(this, "Invalid contact number","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{

            genderId = genderSelection.getActionCommand();
        }
        try{

            ResultSet resultSet = MySQL.execute("INSERT INTO `teacher_regstration` "
                + "(`first_name`,`last_name`,`age`,`gender_gender_id`,`contact_no`,`password`) "
                + "VALUES('"+firstName+"','"+lastName+"','"+age+"','"+genderId+"','"+contactnumber+"','"+password+"')");

            JOptionPane.showMessageDialog(this, "Successfully Registered", "Success", JOptionPane.INFORMATION_MESSAGE);

        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton27ActionPerformed

